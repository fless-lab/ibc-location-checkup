    ARTICLE 2 : BON D’ÉCHANGE
    Le client accepte d’acheter un bon d’échange pour le montant indiqué sur le devis. Il est de sa responsabilité de présenter ce bon d’échange à l’agence au moment de la prise de possession du véhicule.
    Ce bon d'échange est également transmis au client par courrier électronique à l'adresse indiquée dans le champ "email" lors de son inscription.

    Le bon d’échange ne constitue en aucun cas un contrat de location de véhicule. Un contrat de location sera contracté (signé) par le client et l’agence au moment de la prise de possession du véhicule.

    L’agence pourra refuser de contracter avec le client si celui-ci

    - n’a pas l’âge minimum requis

    - n’est pas en possession d’un permis de conduire valable et reconnu

    - ne peut effectuer le dépôt de garantie

    - ne satisfait pas aux conditions générales de location
