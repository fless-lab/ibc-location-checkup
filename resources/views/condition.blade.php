    ARTICLE 3 : TARIF DE LOCATION
    a. Les tarifs de location sont propres aux réservations effectuées par le biais du site internet www.easycarrental.fr. Ces tarifs sont calculés selon les informations fournies par le client le jour de la réservation.

    b. Le tarif faisant l’objet d’une réservation en ligne avec prépaiement ne peut faire l’objet d’une remise et ne peut se cumuler ni avec une autre offre promotionnelle ni avec tout tarif particulier négocié avec un client grand-compte.

    c. Les tarifs excluent tous les frais additionnels et options supplémentaires qui ne sont pas mentionnés dans la confirmation de réservation. Ces frais additionnels devront être payés directement auprès de l’agence.
    <br>
    ARTICLE 4 : VÉHICULES
    Il est rappelé que la réservation porte sur une catégorie de véhicule et non un modèle.

    Toutes les photos présentes sur le site internet ont pour unique but d’illustrer la catégorie et ne constitue donc en aucun cas un engagement sur un modèle bien précis.
    <br>
    ARTICLE 5 : DÉLAIS DE RÉSERVATION SUR LE SITE INTERNET
    Les délais de réservation peuvent varier selon l’agence choisie. Le calendrier permettant de choisir la date/Heure de départ et la Date/Heure de retour tient compte de ce délai et des horaires d’ouvertures de l’agence sélectionnée.
    <br>
    ARTICLE 6 : DURÉE DE LOCATION SUR LE SITE INTERNET
    La durée de location ne peut excéder 29 jours pour les véhicules particuliers et 14 jours pour les véhicules utilitaires.

    Pour des durées supérieures le client devra contacter directement l’agence ou la centrale de réservation.
    <br>
    ARTICLE 7 : PAIEMENT EN LIGNE
    Afin de valider la réservation sur le site internet le client devra payer en une seule fois l’intégralité du montant de la location.

    La fonction « enregistrer ce devis » présent lors de la tarification est une facilité mise à disposition du client afin d’enregistrer un devis ceci dans l’unique but d’améliorer le confort d’utilisation du site internet ; ceci ne constitue donc en aucun cas une réservation ferme.

    Seuls le paiement intégral du montant de la location et la réception par courrier électronique du bon d’échange qui en découle valident la réservation.

    La carte bancaire ayant servi au paiement pourra également vous être demandée lors de la prise de possession du véhicule.
