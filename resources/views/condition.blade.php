    ARTICLE 8 : ANNULATION / REMBOURSEMENT D’UNE RÉSERVATION PRÉPAYÉE[+]
    L’annulation d’une réservation prépayée est possible selon les conditions suivantes :

    a. Si le client annule plus de 72h avant le départ de la location : 10000 fcfa  seront retenus à titre de frais et le restant du montant sera crédité par l’émetteur du bon d’échange sur la carte bancaire ayant servi au paiement sous 10 jours.

    b. Si le client annule moins de 72h avant le départ de la location : aucun remboursement ne sera effectué par l’émetteur ou par l’agence.

    Procédures annulation/remboursement

    [+] Aucun remboursement ne sera effectué si

    a. le client n’utilise que partiellement la période initialement prévue dans la réservation.

    b. Le client effectue des modifications sur la réservation et le nouveau montant est inférieur au montant prépayé.

    c. Le client n'est pas en possession des documents nécessaires ou ne peut effectuer le dépôt de garantie.

    [+] Annulation à l’initiative de l’émetteur ou agence :

    L’émetteur ou l’agence peuvent annuler le bon d’échange (notamment en cas d’indisponibilité du véhicule) et dans ce cas précis l’émetteur procédera à un remboursement intégral du montant prépayé. Le montant sera crédité sur la carte bancaire ayant servi au paiement de la réservation.

